#!/usr/bin/env node
// THIRD-PARTY-LICENSES generator — collects the license texts of the production
// dependency closure (root daemon deps + apps/desktop deps) and emits them as a single manifest.
// No network or extra dependencies (scans node_modules directly).
//
// Usage:  node scripts/gen-third-party-licenses.mjs [outputPath]
//         (default output: ./THIRD-PARTY-LICENSES.txt)  --check for a stale check (for CI).
//
// ⚠️ @anthropic-ai/claude-agent-sdk is proprietary (© Anthropic PBC) — not an OSS license but
//    subject to Anthropic's terms. Its LICENSE.md (all-rights-reserved) is included verbatim here, and noted separately in the header.
import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const args = process.argv.slice(2);
const check = args.includes("--check");
const outPath = path.resolve(ROOT, args.find((a) => !a.startsWith("--")) ?? "THIRD-PARTY-LICENSES.txt");

const LICENSE_FILE = /^(LICEN[SC]E|COPYING|COPYRIGHT)(\..*)?$/i;
const MAX_TEXT = 64 * 1024; // per-package license text cap (guards against pathological packages)

function spdxOf(pj) {
  if (typeof pj.license === "string") return pj.license;
  if (pj.license && typeof pj.license === "object") return pj.license.type ?? "(see license object)";
  if (Array.isArray(pj.licenses)) return pj.licenses.map((l) => l.type ?? l).join(" OR ");
  return "UNKNOWN";
}

// All installed packages: name -> { version, license, deps, dir }
const map = new Map();
function walk(nm) {
  if (!fs.existsSync(nm)) return;
  for (const entry of fs.readdirSync(nm)) {
    if (entry === ".bin" || entry === ".cache") continue;
    const p = path.join(nm, entry);
    if (entry.startsWith("@")) { walk(p); continue; } // scope directory
    const pjPath = path.join(p, "package.json");
    if (fs.existsSync(pjPath)) {
      try {
        const pj = JSON.parse(fs.readFileSync(pjPath, "utf8"));
        if (pj.name && !map.has(pj.name)) {
          map.set(pj.name, { version: pj.version ?? "", license: spdxOf(pj), deps: { ...(pj.dependencies || {}), ...(pj.optionalDependencies || {}) }, dir: p });
        }
      } catch { /* ignore broken package.json */ }
    }
    const nested = path.join(p, "node_modules");
    if (fs.existsSync(nested)) walk(nested);
  }
}
walk(path.join(ROOT, "node_modules"));
walk(path.join(ROOT, "apps/desktop/node_modules"));

// Production closure: BFS from the dependencies/optionalDependencies of root + desktop (devDeps excluded — not shipped in the distribution).
function directDeps(pjRelPath) {
  const pj = JSON.parse(fs.readFileSync(path.join(ROOT, pjRelPath), "utf8"));
  return Object.keys({ ...(pj.dependencies || {}), ...(pj.optionalDependencies || {}) });
}
const seen = new Set();
const queue = [...directDeps("package.json"), ...directDeps("apps/desktop/package.json")];
while (queue.length) {
  const name = queue.shift();
  if (seen.has(name)) continue;
  seen.add(name);
  const e = map.get(name);
  if (e) for (const d of Object.keys(e.deps)) if (!seen.has(d)) queue.push(d);
}

function licenseText(dir) {
  try {
    const f = fs.readdirSync(dir).find((n) => LICENSE_FILE.test(n));
    if (!f) return null;
    return fs.readFileSync(path.join(dir, f), "utf8").slice(0, MAX_TEXT).trimEnd();
  } catch { return null; }
}

const pkgs = [...seen].map((name) => map.get(name) ? { name, ...map.get(name) } : null).filter(Boolean)
  .sort((a, b) => a.name.localeCompare(b.name));

const SEP = "=".repeat(80);
const SUB = "-".repeat(80);
const lines = [];
lines.push("THIRD-PARTY LICENSES for rookery");
lines.push(SEP, "");
lines.push("rookery's own code is licensed under the MIT License (see LICENSE).");
lines.push("This file lists third-party software included in production builds and their");
lines.push("licenses. Generated by scripts/gen-third-party-licenses.mjs — regenerate after");
lines.push("dependency changes (CI: `node scripts/gen-third-party-licenses.mjs --check`).");
lines.push("");
lines.push("NOTE: @anthropic-ai/claude-agent-sdk and its platform binaries are PROPRIETARY");
lines.push("(© Anthropic PBC, all rights reserved) — governed by Anthropic's terms, not an");
lines.push("open-source license. See NOTICE. Binary builds also bundle Electron/Chromium and");
lines.push("a Node.js runtime, each under its own license (see their bundled notices).");
lines.push("");
lines.push(`${pkgs.length} npm packages in the production closure.`);
lines.push("");
for (const p of pkgs) {
  lines.push(SEP);
  lines.push(`${p.name}@${p.version}  —  ${p.license}`);
  lines.push(SUB);
  const txt = licenseText(p.dir);
  lines.push(txt ?? `(No license file bundled; declared license: ${p.license}.)`);
  lines.push("");
}
const out = lines.join("\n") + "\n";

if (check) {
  const cur = fs.existsSync(outPath) ? fs.readFileSync(outPath, "utf8") : "";
  if (cur !== out) {
    console.error(`STALE: ${path.relative(ROOT, outPath)} is out of date — run: node scripts/gen-third-party-licenses.mjs`);
    process.exit(1);
  }
  console.log(`OK: ${path.relative(ROOT, outPath)} up to date (${pkgs.length} packages).`);
} else {
  fs.writeFileSync(outPath, out);
  console.log(`Wrote ${path.relative(ROOT, outPath)} (${pkgs.length} packages, ${(out.length / 1024).toFixed(0)} KB).`);
}
