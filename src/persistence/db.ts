import fs from "node:fs";
import path from "node:path";
import Database from "better-sqlite3";

export type DB = Database.Database;

// Since we're still in pre-release local development, the accumulated migration history was squashed into a single baseline schema (removing past ALTER/RENAME cruft).
// The framework (schema_version + the openDb loop below) is kept — post-release schema changes append a new entry here.
